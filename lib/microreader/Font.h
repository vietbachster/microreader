// Index of '?' in the font table (used as fallback for unknown glyphs).
static constexpr int kFallbackGlyphIdx = '?' - 0x20;

// Decode one UTF-8 codepoint from *p, advance p past it, and return
// the kFont8x8 glyph index.  Returns kFallbackGlyphIdx ('?') for any
// codepoint outside the supported set (printable ASCII + extended).
inline int next_glyph_index(const char*& p) {
  uint8_t b = static_cast<uint8_t>(*p);
  uint32_t cp;
  if (b < 0x80) {
    cp = b;
    p += 1;
  } else if (b < 0xE0) {
    cp = ((b & 0x1F) << 6) | (static_cast<uint8_t>(p[1]) & 0x3F);
    p += 2;
  } else if (b < 0xF0) {
    cp = ((b & 0x0F) << 12) | ((static_cast<uint8_t>(p[1]) & 0x3F) << 6) | (static_cast<uint8_t>(p[2]) & 0x3F);
    p += 3;
  } else {
    cp = ((b & 0x07) << 18) | ((static_cast<uint8_t>(p[1]) & 0x3F) << 12) | ((static_cast<uint8_t>(p[2]) & 0x3F) << 6) |
         (static_cast<uint8_t>(p[3]) & 0x3F);
    p += 4;
  }
  // Printable ASCII
  if (cp >= 0x20 && cp <= 0x7E)
    return static_cast<int>(cp) - 0x20;
  // Extended characters
  switch (cp) {
    case 0x00A7:
      return 95;  // §
    case 0x00A9:
      return 96;  // ©
    case 0x00AB:
      return 97;  // «
    case 0x00BB:
      return 98;  // »
    case 0x00C4:
      return 99;  // Ä
    case 0x00D6:
      return 100;  // Ö
    case 0x00D7:
      return 101;  // ×
    case 0x00DC:
      return 102;  // Ü
    case 0x00DF:
      return 103;  // ß
    case 0x00E1:
      return 104;  // á
    case 0x00E2:
      return 105;  // â
    case 0x00E4:
      return 106;  // ä
    case 0x00E8:
      return 107;  // è
    case 0x00E9:
      return 108;  // é
    case 0x00F6:
      return 109;  // ö
    case 0x00FC:
      return 110;  // ü
    case 0x0142:
      return 111;  // ł
    case 0x017C:
      return 112;  // ż
    case 0x2013:
      return 113;  // –
    case 0x2019:
      return 114;  // '
    case 0x2026:
      return 115;  // …
    case 0x2039:
      return 116;  // ‹
    case 0x203A:
      return 117;  // ›
    case 0x2044:
      return 118;  // ⁄
    default:
      return kFallbackGlyphIdx;
  }
}

// Draw a single character at pixel position (x, y). White pixels on black background.
inline void draw_char(DisplayFrame& frame, int x, int y, char c) {
  int idx = static_cast<unsigned char>(c) - 0x20;
  if (idx < 0 || idx >= detail::kAsciiGlyphCount)
    idx = kFallbackGlyphIdx;
  const auto& glyph = detail::kFont8x8[idx];
  for (int row = 0; row < 8; ++row) {
    for (int col = 0; col < 8; ++col) {
      const bool on = (glyph[row] & (0x80u >> col)) != 0;
      frame.set_pixel(x + col, y + row, on);
    }
  }
}

// Draw a null-terminated UTF-8 string at pixel position (x, y).
// One glyph per codepoint; unknown codepoints render as '?'.
inline void draw_text(DisplayFrame& frame, int x, int y, const char* text) {
  int cx = x;
  while (text && *text) {
    if (cx + 8 > frame.width())
      break;
    int idx = next_glyph_index(text);
    const auto& glyph = detail::kFont8x8[idx];
    for (int row = 0; row < 8; ++row) {
      for (int col = 0; col < 8; ++col) {
        const bool on = (glyph[row] & (0x80u >> col)) != 0;
        frame.set_pixel(cx + col, y + row, on);
      }
    }
    cx += 8;
  }
}

